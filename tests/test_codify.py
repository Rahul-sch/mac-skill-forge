    src = __import__("pathlib").Path(
        "examples/calculator_handwritten/scripts/replay.py"
    ).read_text()
    ast.parse(src)
    assert "from skill_forge.replay.actions" in src
    assert "from skill_forge.replay.ax_resolve" in src
